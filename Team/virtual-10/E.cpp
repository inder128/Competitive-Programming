void solve(){
	int k, n = 8000; cin >> k;
    vector <vector <double>> p(n + 1, vector <double>(k + 1));
    p[0][0] = 1;
    for(int i = 1; i <= n; ++i){
    	for(int j = 1; j <= k; ++j){
    		p[i][j] = p[i - 1][j] * j / k + p[i - 1][j - 1] * (k - (j - 1)) / k;
    	}
    }
    int q; cin >> q;
    while(q--){
    	double pr; cin >> pr;
    	for(int i = 1; i <= n; ++i){
    		if(p[i][k] >= (pr - 1e-7) / 2000){
    			cout << i << el;
    			break;
    		}
    	}
    }
}
 
