const int mod = 998244353;

int add(int x, int y){
    x += y;
    while(x >= mod) x -= mod;
    while(x < 0) x += mod;
    return x;
}

int sub(int x, int y){
    return add(x, -y);
}

int mul(int x, int y){
    return (x * 1ll * y) % mod;
}

int binPow(int x, int y){
    int z = 1;
    while(y){
        if(y & 1) z = mul(z, x);
        x = mul(x, x);
        y >>= 1;
    }
    return z;
}

int inv(int x){
    return binPow(x, mod - 2);
}

int divide(int x, int y){
    return mul(x, inv(y));
}


void solve(){
    string s, t; cin >> s >> t;
    int n = SZ(s), m = SZ(t);
    t.insert(t.end(), n - m, '$');
    vvi DP(n, vi(n));
    for(int i = 0; i < n; ++i){
    	if(t[i] == '$' or t[i] == s[0]){
    		DP[i][i] = 2;
    	}
    }
    for(int l = 2; l <= n; ++l){
    	for(int i = 0, j = l - 1; j < n; ++i, ++j){
    		DP[i][j] = (t[i] == '$' or s[l - 1] == t[i]) * DP[i + 1][j];
    		DP[i][j] = add(DP[i][j], (t[j] == '$' or s[l - 1] == t[j]) * DP[i][j - 1]);
    	}
    }
    int ans = 0;
    for(int i = m - 1; i <= n - 1; ++i){
    	ans = add(ans, DP[0][i]);
    }
    cout << ans << el;
}
 
