void solve(){
    int as = 0, ts = 0;
    int n; cin >> n;

    vector <pi> at(n);
    for(int i = 0; i < n; ++i){
    	cin >> at[i].F >> at[i].S;
    	as += at[i].F;
    }

    sort(rng(at), [&](const pi& p1, const pi& p2){
    	return 2 * p1.F + p1.S > 2 * p2.F + p2.S;
    });

    int ans = 0;
    while(ts <= as){

    	ts += at[ans].F + at[ans].S;
    	as -= at[ans].F;

    	ans++;
    }


    cout << ans << el;
}
 
